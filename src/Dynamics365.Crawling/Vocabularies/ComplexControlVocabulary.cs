                    .WithDescription(@"Unique identifier of the complex control.")
                    .WithDisplayName("ComplexControlId")
                    .ModelProperty("complexcontrolid", typeof(Guid)));

                this.ComplexControlXml = group.Add(new VocabularyKey("ComplexControlXml", VocabularyKeyDataType.Text, VocabularyKeyVisibility.Visible)
                    .WithDataAnnotations(k => k.NotEditable(), k => k.MaxLength(1073741823))
                    .WithDescription(@"XML representation of the complex control layout.")
                    .WithDisplayName("ComplexControlXml")
                    .ModelProperty("complexcontrolxml", typeof(string)));

                this.Description = group.Add(new VocabularyKey("Description", VocabularyKeyDataType.Text, VocabularyKeyVisibility.Visible)
                    .WithDataAnnotations(k => k.NotEditable(), k => k.MaxLength(2000))
                    .WithDescription(@"Description of the complex control.")
                    .WithDisplayName("Description")
                    .ModelProperty("description", typeof(string)));

                this.Name = group.Add(new VocabularyKey("Name", VocabularyKeyDataType.Text, VocabularyKeyVisibility.Visible)
                    .WithDataAnnotations(k => k.NotEditable(), k => k.MaxLength(100))
