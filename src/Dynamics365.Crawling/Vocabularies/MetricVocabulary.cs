                    .WithDataAnnotations(k => k.CanEdit())
                    .WithDescription(@"Information that indicates whether the metric type is Count or Amount.")
                    .WithDisplayName("Metric Type")
                    .ModelProperty("isamount", typeof(bool)));

                this.IsAmountName = group.Add(new VocabularyKey("IsAmountName", VocabularyKeyDataType.Text, VocabularyKeyVisibility.Visible)
                    .WithDataAnnotations(k => k.CanEdit())
                    .WithDescription(@"")
                    .WithDisplayName("IsAmountName")
                    .ModelProperty("isamountname", typeof(string)));

                this.IsStretchTrackedName = group.Add(new VocabularyKey("IsStretchTrackedName", VocabularyKeyDataType.Text, VocabularyKeyVisibility.Visible)
                    .WithDataAnnotations(k => k.CanEdit())
                    .WithDescription(@"")
                    .WithDisplayName("IsStretchTrackedName")
                    .ModelProperty("isstretchtrackedname", typeof(string)));

