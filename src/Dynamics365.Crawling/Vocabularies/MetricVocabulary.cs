                    .WithDataAnnotations(k => k.NotEditable())
                    .WithDescription(@"Indicates whether the goal metric tracks stretch targets.")
                    .WithDisplayName("Track Stretch Target")
                    .ModelProperty("isstretchtracked", typeof(bool)));

