                    .WithDataAnnotations(k => k.NotEditable(), k => k.MaxLength(4000))
                    .WithDescription(@"Shows the title of the knowledge base (KB) Record.")
                    .WithDisplayName("KB Record Title")
                    .ModelProperty("title", typeof(string)));

                this.PrivateUrl = group.Add(new VocabularyKey("PrivateUrl", VocabularyKeyDataType.Uri, VocabularyKeyVisibility.Visible)
                    .WithDataAnnotations(k => k.NotEditable(), k => k.MaxLength(256))
                    .WithDescription(@"Shows the internal Parature service desk URL of the knowledge base records.")
                    .WithDisplayName("Private URL")
                    .ModelProperty("privateurl", typeof(string)));

                this.PublicUrl = group.Add(new VocabularyKey("PublicUrl", VocabularyKeyDataType.Uri, VocabularyKeyVisibility.Visible)
                    .WithDataAnnotations(k => k.NotEditable(), k => k.MaxLength(256))
                    .WithDescription(@"Shows the public Parature portal URL of the knowledge base records.")
